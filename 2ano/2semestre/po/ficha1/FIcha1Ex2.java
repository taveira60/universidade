public class FIcha1Ex2 {
    public double celsiusToFarenheit(double graus) {
        return graus * 1.8 + 32;
    }

    public int maximoDeDoisNumeros(int a, int b) {
        return Math.max(a, b);
    }

    public String criaDescricaoConta(String nome, String saldo) {
        return "Nome: " + nome + " saldo: " + saldo;
    }
}
